    public void arithemetic_operation(){
        try{
            switch(calculation)
        {       
            case 1: 
                ans = num + Double.parseDouble(txtField.getText());
                txtField.setText(Double.toString(ans));
                break;
            case 2: 
                ans = num - Double.parseDouble(txtField.getText());
                txtField.setText(Double.toString(ans));
                break;
            case 3: 
                ans = num * Double.parseDouble(txtField.getText());
                txtField.setText(Double.toString(ans));
                break;
            case 4: 
                ans = num / Double.parseDouble(txtField.getText());
                txtField.setText(Double.toString(ans));
                break;
        }
        } catch(Exception e) {
            System.out.println("This program will give an error.");
            //JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }    
